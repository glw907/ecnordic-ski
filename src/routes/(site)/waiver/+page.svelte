        <div class="risk-item"><strong>Running</strong>Falls; sprains, strains, overuse injuries; collisions with vehicles, cyclists, pedestrians, or wildlife; heat illness; hypothermia; getting lost on trails; moose, bear, or other wildlife encounters.</div>
        <div class="risk-item"><strong>Mountain Biking</strong>Crashes and falls at speed; collisions with vehicles, other riders, pedestrians, trees, rocks, and obstacles; mechanical failure; head, neck, spine, and orthopedic injuries; encounters with wildlife.</div>
