      <p class="body-text">I understand and acknowledge that the activities listed carry <strong>inherent risks that cannot be eliminated</strong> regardless of the care taken by volunteer coaches or other adults. Pursuant to <strong>Alaska Statute 09.65.290</strong>, a person who participates in a sports or recreational activity assumes the inherent risks of that activity. Inherent risks include, without limitation:</p>
